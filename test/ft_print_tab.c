#include "ft_print_tab.h"
#include <unistd.h>

void	print_tab(int **tab)
{
	int	x;
	int	y;

	y = 0;
	while (y < 6)
	{
		x = 0;
		while (x < 6)
		{
			ft_putchar(tab[y][x] + '0');
			ft_putchar(' ');
			x++;
		}
		write (1, "\n", 1);
		y++;
	}
}
